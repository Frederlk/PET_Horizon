//Menu
let iconMenu = document.querySelector(".icon-menu");

if (document.querySelector('.menu__wrapper')) {
	let menuWrapper = document.querySelector(".menu__wrapper");
	menuWrapper.addEventListener("click", function (e) {
		if (!e.target.closest('.menu__body')) {
			menu_close(e.target.closest('.menu__wrapper'));
			body_lock(delay);
		}
	});
};
let delay = 500;
if (iconMenu != null) {
	let menuBody = document.querySelector(".menu__body");
	iconMenu.addEventListener("click", function (e) {
		if (unlock) {
			body_lock(delay);
			iconMenu.classList.toggle("_active");
			menuBody.classList.toggle("_active");
			if (document.querySelector('.menu__wrapper')) {
				menuWrapper.classList.toggle("_active");
			};
		};
	});
};
function menu_close() {
	let iconMenu = document.querySelector(".icon-menu");
	let menuBody = document.querySelector(".menu__body");
	iconMenu.classList.remove("_active");
	menuBody.classList.remove("_active");
	if (document.querySelector('.menu__wrapper')) {
		menuWrapper.classList.remove("_active");
	};
};
document.addEventListener('keydown', function (e) {
	if (e.code === 'Escape') {
		menu_close();
	}
});
